function BindAreas() {
    var areas = GetAreas();
    $.each(areas, function (index, value) {
        $('#cmbArea').append($('<option/>', {
            value: value.AreaId,
            text: value.AreaName
        }));
    });
}

function getAreaByAreaId(areaId) {
    var areas = GetAreas();
    var foundArea = areas.find(a => a.AreaId === areaId);
    return foundArea;
}